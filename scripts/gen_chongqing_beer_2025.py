    for c in range(1, 5): apply_style(ws, 2, c, header_fill, header_font)
    headers = ["序号", "数据项", "数据值", "数据来源/说明"]
    for i, h in enumerate(headers, 1):
        ws.cell(row=3, column=i, value=h); apply_style(ws, 3, i, header_fill, header_font)
    for idx, (seq, name, value, source) in enumerate(data_items):
        row = 4 + idx
        ws.cell(row=row, column=1, value=seq); ws.cell(row=row, column=2, value=name)
