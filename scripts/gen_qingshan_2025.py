def create_table1():
    wb = Workbook()
    ws = wb.active
    ws.title = "22项年报数据"

    # Column widths
    for i, w in enumerate(COL_WIDTHS, 1):
        ws.column_dimensions[get_column_letter(i)].width = w

    # Row 1: Title
    ws.merge_cells("A1:D1")
    ws.cell(row=1, column=1, value="青山纸业(600103.SH) 2025年年报 22项核心数据")
    for c in range(1, 5):
        apply_style(ws, 1, c, title_fill, title_font)

    # Row 2: Subtitle
    ws.merge_cells("A2:D2")
    ws.cell(row=2, column=1, value="数据来源: 公司2025年年度报告(2026-04-13发布) + ProSearch全网搜索 | 货币单位: 人民币 | 数据日期: 2026-04-25")
    for c in range(1, 5):
        apply_style(ws, 2, c, header_fill, header_font)

    # Row 3: Headers
    headers = ["序号", "数据项", "数据值", "数据来源/说明"]
    for i, h in enumerate(headers, 1):
        ws.cell(row=3, column=i, value=h)
        apply_style(ws, 3, i, header_fill, header_font)

    # Data rows
    for idx, (seq, name, value, source) in enumerate(data_items):
        row = 4 + idx
        ws.cell(row=row, column=1, value=seq)
        ws.cell(row=row, column=2, value=name)
        ws.cell(row=row, column=3, value=value)
        ws.cell(row=row, column=4, value=source)
        for c in range(1, 5):
            apply_style(ws, row, c, white_fill, data_font)
        ws.row_dimensions[row].height = ROW_HEIGHT_DATA

    # Freeze panes
    ws.freeze_panes = "A4"

    return wb


