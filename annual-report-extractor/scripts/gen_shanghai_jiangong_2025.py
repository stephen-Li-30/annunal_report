TITLE_FILL = PatternFill('solid', fgColor='006B5A')
HEADER_FILL = PatternFill('solid', fgColor='00E8F5F0')
DATA_FILL = PatternFill('solid', fgColor='FFFFFF')

TITLE_FONT = Font(name='Arial', size=12, bold=True, color='FFFFFF')
HEADER_FONT = Font(name='Arial', size=10, bold=False, color='000000')
DATA_FONT = Font(name='Arial', size=10, bold=False, color='000000')
DATE_FONT = Font(name='Arial', size=9, color='666666')

THIN_BORDER = Border(
    left=Side(style='thin', color='CCCCCC'),
    right=Side(style='thin', color='CCCCCC'),
    top=Side(style='thin', color='CCCCCC'),
    bottom=Side(style='thin', color='CCCCCC')
)

COL_WIDTHS = {'A': 5.0, 'B': 38.0, 'C': 65.0, 'D': 45.0}
ROW_HEIGHT_DATA = 100

def apply_style(ws, row, col, font=DATA_FONT, fill=DATA_FILL, alignment=None):
    cell = ws.cell(row=row, column=col)
    cell.font = font
    cell.fill = fill
    cell.border = THIN_BORDER
    if alignment:
        cell.alignment = alignment
    else:
        cell.alignment = Alignment(wrap_text=True, vertical='top')

def setup_sheet(ws, title, date_str):
    """设置Sheet通用样式：标题行、日期行、表头行"""
    # 列宽
    for col_letter, width in COL_WIDTHS.items():
        ws.column_dimensions[col_letter].width = width

    # 标题行 (合并A1:D1)
    ws.merge_cells('A1:D1')
    cell = ws.cell(row=1, column=1, value=title)
    cell.font = TITLE_FONT
    cell.fill = TITLE_FILL
    cell.alignment = Alignment(horizontal='center', vertical='center')
    ws.row_dimensions[1].height = 30
    for c in range(1, 5):
        ws.cell(row=1, column=c).fill = TITLE_FILL
        ws.cell(row=1, column=c).border = THIN_BORDER

    # 日期行 (合并A2:D2)
    ws.merge_cells('A2:D2')
    cell = ws.cell(row=2, column=1, value=date_str)
    cell.font = DATE_FONT
    cell.fill = DATA_FILL
    cell.alignment = Alignment(horizontal='left', vertical='center')
    for c in range(1, 5):
        ws.cell(row=2, column=c).border = THIN_BORDER

    # 表头行
    headers = ['序号', '数据项', '数据内容', '数据来源/说明']
    for i, h in enumerate(headers, 1):
        cell = ws.cell(row=3, column=i, value=h)
        cell.font = HEADER_FONT
        cell.fill = HEADER_FILL
        cell.border = THIN_BORDER
        cell.alignment = Alignment(horizontal='center', vertical='center')

    # 冻结窗格
    ws.freeze_panes = 'A4'


